    private void txt_PasswordKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_PasswordKeyPressed
        // TODO add your handling code here:
        
        
        //char c =evt.getKeyChar(); //this char c is not used by KB
        String password=txt_Password.getText();
        int d =txt_Password.getText().length();
        
        boolean hasNum=false;
        boolean hasCap=false;
        boolean hasLow=false;
        char c;
        
        
        if(d!=7){
            lbl_validate.setText("*Given password is not valid.");
            
            
        }
        else{
            txt_Password.setEditable(true);
            lbl_validate.setText("*Given password is valid");
       
        }
        
        
        for(int i =0; i<d;i++)
        {
            c = password.charAt(i);
            if(Character.isDigit(c)){
                hasNum=true;
            }
            
            if(Character.isUpperCase(c)){
                hasCap=true;
            }
            
            if(Character.isLowerCase(c)){
                hasLow=true;
            }
            if(hasNum && hasCap && hasLow){
                lbl_validate.setText("*Given password is valid");
            }else{
            lbl_validate.setText("*Given password is not valid.");
            }
        }
       
        
               
       


        
    }//GEN-LAST:event_txt_PasswordKeyPressed

