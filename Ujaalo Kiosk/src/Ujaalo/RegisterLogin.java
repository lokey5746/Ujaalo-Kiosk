    public void Register(){
        
        conn = OracleDatabase.ConnectDb();
       
       try{
           
           String sql = "insert into Student (UniversityID,StdName,StdEmail,Password,EnrolledCourse) values(?,?,?,?,?)";
           pst = (OraclePreparedStatement) conn.prepareStatement(sql);
           pst.setString(1,txt_UniID.getText());
           pst.setString(2,FullName.getText());
           pst.setString(3,txt_Email.getText());
           pst.setString(4,txt_Password.getText());
          // pst.setString(5,txt_EnrolledCourses.getText());
           
           rs =(OracleResultSet) pst.executeQuery();
           
           if (rs.next())
           {
               JOptionPane.showMessageDialog(null,"Registeration Successful.Now you will be redirected to login page");
               
                Login lgf = new Login();
                lgf.setVisible(true);
                this.dispose();
           }
           
           else
           {
               JOptionPane.showMessageDialog(null,"Registeration Unsuccessful.Please fill the registeration form carefully!!");
           }
          
       }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
        
    }
    
    
    
    
